from flask import Flask, render_template, request, redirect, url_for
from db import create_table, add_student, get_all_students, get_student_by_id, update_student_marks, delete_student

app = Flask(__name__)

# Create the students table if it doesn't exist
create_table()


@app.route('/')
def index():
    """Display all students and their marks."""
    students = get_all_students()  # Fetch all students from the database
    return render_template('index.html', students=students)


@app.route('/add', methods=['GET', 'POST'])
def add():
    """Add a new student."""
    if request.method == 'POST':
        student_name = request.form['student_name']
        marks = request.form['marks']

        # Add the student to the database
        add_student(student_name, int(marks))
        return redirect(url_for('index'))  # Redirect to the homepage after adding

    return render_template('add.html')


@app.route('/edit/<int:student_id>', methods=['GET', 'POST'])
def edit(student_id):
    """Edit the marks of an existing student."""
    student = get_student_by_id(student_id)  # Fetch the student by ID

    if request.method == 'POST':
        new_marks = request.form['marks']

        # Update the student's marks
        update_student_marks(student_id, int(new_marks))
        return redirect(url_for('index'))  # Redirect to the homepage after updating

    return render_template('edit.html', student=student)


@app.route('/delete/<int:student_id>', methods=['POST'])
def delete(student_id):
    """Delete a student."""
    delete_student(student_id)  # Delete the student by ID
    return redirect(url_for('index'))  # Redirect to the homepage after deletion


if __name__ == '__main__':
    # Run without debug mode
    app.run()  # Default is debug=False














































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































